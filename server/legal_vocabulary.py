"""
Юр-словарь для русского судебного домена.

Используется в двух местах:
  1) LEGAL_PROMPT — короткая подсказка для Whisper (~150 слов, лимит ~244 токена).
     Помогает модели лучше «слышать» терминологию.
  2) CORRECTIONS — пост-обработка после распознавания, исправляет
     типичные ошибки Whisper для русского юр-домена.
"""
from __future__ import annotations

import re

LEGAL_PROMPT = (
    "Председательствующий судья, государственный обвинитель, прокурор, "
    "защитник, адвокат, подсудимый, обвиняемый, потерпевший, свидетель, "
    "эксперт, специалист, секретарь судебного заседания, пристав. "
    "Уголовный кодекс Российской Федерации, Уголовно-процессуальный кодекс, "
    "Гражданский кодекс, Гражданско-процессуальный кодекс, "
    "Кодекс об административных правонарушениях. "
    "Статья, часть, пункт, постановление, определение, приговор, "
    "ходатайство, заявление, возражение, апелляция, кассация, протокол, "
    "обвинительное заключение, материалы уголовного дела, "
    "вещественные доказательства, экспертиза. "
    "Председательствующий объявляет состав суда. "
    "Слово предоставляется государственному обвинителю. "
    "Защита поддерживает позицию доверителя. "
    "Свидетель предупреждён об ответственности по статьям 307, 308 УК РФ. "
    "Суд удаляется в совещательную комнату для постановления приговора."
)

# Типичные ошибки распознавания Whisper для русского юр-домена.
# Формат: (regex_pattern_pre_strip, replacement). Регистронезависимо.
_CORRECTIONS: list[tuple[str, str]] = [
    # Подсудимый — Whisper часто разрывает на части
    (r"\bпод\s*судим(ый|ого|ому|ым|ом|ая|ой|ую|ое)\b", r"подсудим\1"),
    (r"\bподс?удим(ый|ого|ому|ым|ом|ая|ой|ую|ое)\b", r"подсудим\1"),
    # Обвиняемый
    (r"\bобвинЯ?\s+емый\b", "обвиняемый"),
    # Прокурор
    (r"\bпрак[ау]рор\b", "прокурор"),
    # Председательствующий
    (r"\bпредседательству[ющ]+ий\b", "председательствующий"),
    # Гос. обвинитель
    (r"\bгос[\s\.]+обвинител", "государственный обвинител"),
    # УК РФ / УПК РФ / ГК РФ — расшифровки слитные
    (r"\bу\.?\s*к\.?\s+р\.?\s*ф\.?\b", "УК РФ"),
    (r"\bу\.?\s*п\.?\s*к\.?\s+р\.?\s*ф\.?\b", "УПК РФ"),
    (r"\bг\.?\s*к\.?\s+р\.?\s*ф\.?\b", "ГК РФ"),
    (r"\bг\.?\s*п\.?\s*к\.?\s+р\.?\s*ф\.?\b", "ГПК РФ"),
    # Судебное заседание
    (r"\bсудебном\s+зас[еи]дани[еияй]+\b", "судебном заседании"),
    # Совещательная комната
    (r"\bсовещательн[оа]й\s+ком[оан]+те?\b", "совещательной комнате"),
    # Ходатайство
    (r"\bход[ао]?тайств[ао]\b", "ходатайство"),
    # Возражение — частое «возр ажение»
    (r"\bвозр\s+ажени(е|я|и|ю|ем)\b", r"возражени\1"),
]

_COMPILED = [(re.compile(p, re.IGNORECASE), r) for p, r in _CORRECTIONS]


def apply_corrections(text: str) -> str:
    if not text:
        return text
    result = text
    for pattern, replacement in _COMPILED:
        result = pattern.sub(replacement, result)
    return result


def build_initial_prompt(user_prompt: str | None) -> str:
    """
    Юр-словарь + пользовательская подсказка (имена спикеров, специфика дела).
    Whisper берёт последние ~244 токена prompt'а как контекст,
    поэтому user_prompt идёт В КОНЦЕ — ближе к началу аудио.
    """
    parts = [LEGAL_PROMPT]
    if user_prompt:
        parts.append(user_prompt.strip())
    return " ".join(parts)
